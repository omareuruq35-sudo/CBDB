//  2. normalize (خليه كده زي ما developer قال)
function normalize(text) {
  if (!text) return "";

  return text
    .toLowerCase()
    .replace(/[^\u0600-\u06FFa-z0-9\s]/g, " ")
    .replace(/\s+/g, " ")
    .replace(/(dam|blood)/g, "دم")
    .replace(/(tabaro3|donate)/g, "تبرع")
    .trim();
}





